// Primitive types
// String, Number, Boolean, Null, Undefined, Symbol, BigInt

// String
let name = "John Doe"; // String
let age = 30; // Number
let isStudent = false; // Boolean
let address = null; // Null
let phoneNumber; // Undefined
let uniqueID = Symbol("id"); // Symbol
let largeNumber = BigInt(1234567890123456789012345678901234567890); // BigInt

console.log(name, typeof name); // "string"
console.log(age, typeof age); // "number"
console.log(isStudent, typeof isStudent); // "boolean"
console.log(address, typeof address); // "object"
console.log(phoneNumber, typeof phoneNumber); // "undefined"
console.log(uniqueID, typeof uniqueID); // "symbol"
console.log(largeNumber, typeof largeNumber); // "bigint"